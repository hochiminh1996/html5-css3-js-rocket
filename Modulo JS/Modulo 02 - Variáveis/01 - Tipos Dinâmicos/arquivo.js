let a = "Felippe";//definido como string
console.log(typeof (a));

a = 12;// agora é um number
console.log(typeof (a));

a = 12.3; // agora é um number do tipo float ou real ou decimal
console.log(typeof (a));

a = false; // agora é um boolean
console.log(typeof (a))

