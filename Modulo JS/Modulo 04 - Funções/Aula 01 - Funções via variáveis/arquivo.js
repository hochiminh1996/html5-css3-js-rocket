

// usando uma variável para armazenar uma função. Também conhecido como FUNÇÃO ANÔNIMA, já que a function não tem nome, apenas uma referência em memória (via variável  )



// definindo uma variável constante que armazenará um function. Na function temos os parâmetros. Parâmetros são n1 e n2

const sum = function (n1, n2) {
    return n1 + n2;
}



//chamando a função e passando argumentos. Argumentos são 10 e 15. 
console.log(sum(10, 15));


// Passando valores é argumentado, a exemplo do 10 e 15. Dentro da função, as variáveis n1 e n2, que armazenará os argumentos, serão classificadas como parâmetros.

