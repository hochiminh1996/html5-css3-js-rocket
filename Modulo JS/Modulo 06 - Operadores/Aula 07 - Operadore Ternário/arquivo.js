/*
    Operador Ternário

    condição ? resultado 1 : resultado 2

*/

let idade = 12, bread = true, milk = true;


console.log(idade >= 18 ? "Maior de idade" : "Menor de Idade");

console.log(bread && milk ? "Café da manhã básico" : "Café da manhã ruim");
console.log(bread || milk ? "Café da manhã básico" : "Café da manhã ruim");