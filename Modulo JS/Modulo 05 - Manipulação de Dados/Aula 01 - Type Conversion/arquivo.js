/*
    Type Conversion (typecasting) vs Type Coersion
 
    Alteração de um tipo de dado para outro tipo

    Type Coersion - > O próprio JS fazendo a conversão de dados
    Type Conversion -> Nós realizando a modificação do tipo de dado

 */



console.log("8"+5);// T. Coersion ->  O JS converte o 5 em string e concatena 
console.log(Number("8")+5); // T. Conversion -> Eu manualmente faço a conversão do dado para number. 
